func TestBuild_DetectMaggieDir_ReturnsNonEmpty(t *testing.T) {
	dir := detectMaggieDir()
	if dir == "" {
		t.Fatal("detectMaggieDir returned empty string")
	}
	// When running from the repo, it should point to the repo root
	// which contains go.mod.
	goMod := filepath.Join(dir, "go.mod")
	if _, err := os.Stat(goMod); err != nil {
		t.Errorf("expected go.mod at %s, got error: %v", goMod, err)
	}
}

func TestBuild_DetectMaggieDir_FallsBackToEnv(t *testing.T) {
	// When MAGGIE_DIR is set, it should be used as a fallback.
	// We can't easily force runtime.Caller to fail, but we can verify
	// the env var path exists in the function and that the primary
	// path (runtime.Caller) takes precedence.
	orig := os.Getenv("MAGGIE_DIR")
	defer os.Setenv("MAGGIE_DIR", orig)

	os.Setenv("MAGGIE_DIR", "/tmp/fake-maggie")
	dir := detectMaggieDir()
	// runtime.Caller succeeds in tests, so env var should NOT be used
	if dir == "/tmp/fake-maggie" {
		t.Error("expected runtime.Caller path to take precedence over MAGGIE_DIR")
	}
}

