func TestBuild_MapWrapDirsToImports_ValidWrapGo(t *testing.T) {
	dir := t.TempDir()

	// Create a subdirectory with a valid wrap.go
	subdir := filepath.Join(dir, "http")
	os.MkdirAll(subdir, 0o755)
	wrapContent := "// Code generated by mag wrap net/http. DO NOT EDIT.\npackage wrap_http\n"
	os.WriteFile(filepath.Join(subdir, "wrap.go"), []byte(wrapContent), 0o644)

	result := mapWrapDirsToImports(dir)

	if len(result) != 1 {
		t.Fatalf("expected 1 entry, got %d: %v", len(result), result)
	}
	gotDir, ok := result["net/http"]
	if !ok {
		t.Fatalf("expected key 'net/http', got keys: %v", result)
	}
	if gotDir != subdir {
		t.Errorf("expected dir %s, got %s", subdir, gotDir)
	}
}

func TestBuild_MapWrapDirsToImports_MultiplePackages(t *testing.T) {
	dir := t.TempDir()

	packages := []struct {
		subdir     string
		importPath string
	}{
		{"http", "net/http"},
		{"json", "encoding/json"},
		{"fmt", "fmt"},
	}

	for _, pkg := range packages {
		subdir := filepath.Join(dir, pkg.subdir)
		os.MkdirAll(subdir, 0o755)
		content := "// Code generated by mag wrap " + pkg.importPath + ". DO NOT EDIT.\npackage wrap_" + pkg.subdir + "\n"
		os.WriteFile(filepath.Join(subdir, "wrap.go"), []byte(content), 0o644)
	}

	result := mapWrapDirsToImports(dir)

	if len(result) != 3 {
		t.Fatalf("expected 3 entries, got %d: %v", len(result), result)
	}
	for _, pkg := range packages {
		gotDir, ok := result[pkg.importPath]
		if !ok {
			t.Errorf("missing key %q", pkg.importPath)
			continue
		}
		expected := filepath.Join(dir, pkg.subdir)
		if gotDir != expected {
			t.Errorf("for %q: expected dir %s, got %s", pkg.importPath, expected, gotDir)
		}
	}
}

